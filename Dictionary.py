#------------------------Dictionary------------------------

'''Python dictionary is an unordered collection of items.
While other compound data types have only value as an element , a dictionary has a key :vakue pair.'''


marks={'Ankita': 80, 'Akash': 90, 'Srishti': 80}
print(marks)

print(marks['Ankita']) #accessing value using key

marks2={'Ankita': 80, 'Akash': 90, 'Srishti': 80, 20:100}
print(marks2[20]) #accessing value using key