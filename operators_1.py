# Operators in python


#Arithmetic Operators

#taking values in variables
a=5
b=2


print('using Arithmetic Operators :')
print(a+b)  #add
print(a-b)  #subtract
print(a*b)  #multiply
print(a/b)  #divide

print(a%b)  #modulo:gives the remainder
print(a//b)  #quotient
print(a**b)  #a to the power b

print('-------------------------------------------------------')

#Assignment Operators

print('using Assignment Operators :')
a+=5  # a= a+5
print(a)
b-=2  #b= b-2
print(b)
a*=5 #a=a*5  (10*5)
print(a)
a/=2  #a=a/2 (50/2)
print(a)
