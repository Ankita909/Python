# Operators in python

#Relational/Comparison Operators : returns either true or false

first_no=20
sec_no=40

print('using realational operators :')

print(first_no > sec_no) 
print(first_no < sec_no)
print(first_no == sec_no)   #is first no. equal to the second number
print (first_no != sec_no)  #not equal to
print(first_no >= sec_no)  
print(first_no <= sec_no)
print('-------')
